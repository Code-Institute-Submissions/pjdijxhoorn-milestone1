
//--------event block-1---------//
document.getElementById("event-1").addEventListener("mouseover", mouseOver1);
document.getElementById("event-1").addEventListener("mouseout", mouseOut1);

function mouseOver1() {
    document.getElementById("history-explain-block-1").style.display = "block";
}
function mouseOut1() {
    document.getElementById("history-explain-block-1").style.display = "none";}


